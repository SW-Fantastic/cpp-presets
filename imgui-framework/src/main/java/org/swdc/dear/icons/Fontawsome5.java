    public static void loadFonts(ImGuiIO io, double scale) {

        if (buffer != null) {
            return;
        }

        Module module = Fontawsome5.class.getModule();
        try {

            InputStream is = module.getResourceAsStream(location);
            byte[] data = is.readAllBytes();
            buffer = new BytePointer(data);

            ImFontConfig config = DearUtils.createFontConfig(true,true);
            ImGUICore.ImFontAtlas_AddFontFromMemoryTTF(
                    io.Fonts(),
                    buffer,
                    data.length,
                    (float) (18 * scale), // 也就是24 * 屏幕的缩放率。
                    config,
                    ImGUICore.Ext_CreateFontRange('\ue000','\uf8ff')
            );

        } catch (Exception e) {
            throw new RuntimeException(e);
        }

    }

